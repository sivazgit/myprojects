// Operators

// 1. Assignment operator : =

var no1 = 30
var no2 = 20
console.log("assign operator = : no1=",no1);

//2. Arithmetic operator : + - * / % **
console.log("no1 % (modulus) 3 =",no1%3);

console.log("no1 ** (exponent) 3 =",no1**3);

// 3. Relational operators: < > <= >= == !=
console.log("no1<no2",no1<no2);
console.log("no1>no2",no1>no2);
console.log("no1<=no2",no1<=no2);
console.log("no1>=no2",no1>=no2);
console.log("no1!=no2",no1!=no2);
console.log("no1==no2",no1==no2);

// 3. Increment and Decrement operators : ++ --
console.log("no1++",no1++);
console.log("no1 after increment = ",no1)

console.log("++no2",++no2)
console.log("no2 after increment = ",no2)

